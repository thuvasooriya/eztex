// write newline-separated init seed list to buffer.
// returns bytes written, or 0 if buffer too small.
pub fn write_init_seed_list(out: [*]u8, cap: usize) usize {
    var pos: usize = 0;
    for (init) |name| {
        if (pos + name.len + 1 > cap) return 0;
        @memcpy(out[pos..][0..name.len], name);
        pos += name.len;
        out[pos] = 10;
        pos += 1;
    }
    return pos;
}

// write newline-separated format generation seed list to buffer.
pub fn write_format_gen_seed_list(out: [*]u8, cap: usize) usize {
    var pos: usize = 0;
    for (xelatex_fmt) |name| {
        if (pos + name.len + 1 > cap) return 0;
        @memcpy(out[pos..][0..name.len], name);
        pos += name.len;
        out[pos] = 10;
        pos += 1;
    }
    return pos;
}
